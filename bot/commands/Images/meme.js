const { MessageEmbed, Collector } = require('discord.js');
const got = require('got');
const { colors } = require('../../utils/tools');

module.exports.run = (bot, message) => {
    const embed = new MessageEmbed();
    got('https://www.reddit.com/r/memes/random/.json').then(response => {
        let content = JSON.parse(response.body);
        let permalink = content[0].data.children[0].data.permalink;
        let memeUrl = `https://reddit.com${permalink}`;
        let memeImage = content[0].data.children[0].data.url;
        let memeTitle = content[0].data.children[0].data.title;
        let memeUpvotes = content[0].data.children[0].data.ups;
        let memeDownvotes = content[0].data.children[0].data.downs;
        let memeNumComments = content[0].data.children[0].data.num_comments;
        embed.setTitle(`${memeTitle}`)
        embed.setURL(`${memeUrl}`)
        embed.setImage(memeImage)
        embed.setColor(colors[Math.floor(Math.random() * colors.length)]);
        embed.setFooter(`ğŸ‘ ${memeUpvotes} ğŸ‘ ${memeDownvotes} ğŸ’¬ ${memeNumComments}`)
        message.channel.send(embed);
    });
}

module.exports.help = {
    name: "meme",
    description: "I will generete memes",
    category: "Images"
}